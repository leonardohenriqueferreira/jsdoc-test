/** 
 * Calculator module
 * @module calculator
 * @tutorial calculator
 */

/**
 * 
 * @param {number} value1 Value for the calculation
 * @param {number} value2 Value for the calculation
 * @returns {number} Returns the sum of two numbers
 * @tutorial calculator-addition
 */
exports.addition = (value1, value2) => {
    return value1 + value2
}

/**
 * 
 * @param {number} value1 Value for the calculation
 * @param {number} value2 Value for the calculation
 * @returns {number} Returns the subtraction of two numbers
 * @tutorial calculator-subtraction
 */
exports.subtraction = (value1, value2) => {
    return value1 - value2
}
